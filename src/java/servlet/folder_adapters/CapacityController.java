package servlet.folder_adapters;

import java.io.File;

/**
 *
 * @author User
 */
public class CapacityController {

    public long getDirSize(File dir) {
        long size = 0;
        if (dir.isFile()) {
            size = dir.length();
        } else {
            File[] subFiles = dir.listFiles();
            for (File file : subFiles) {
                if (file.isFile()) {
                    size += file.length();
                } else {
                    size += getDirSize(file);
                }
            }
        }
        size /= 1048576;
        return size;
    }
}
